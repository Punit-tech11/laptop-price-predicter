@app.route('/')
def index():
    return render_template('index.html')

@app.route('/api/laptops')
def get_laptops():
    # Advanced filtering
    query_params = request.args
    filtered_laptops = sample_laptops

    if 'brand' in query_params:
        brands = query_params.get('brand').split(',')
        filtered_laptops = [l for l in filtered_laptops if l['brand'] in brands]
    
    if 'min_ram' in query_params:
        min_ram = int(query_params.get('min_ram'))
        filtered_laptops = [l for l in filtered_laptops if l['ram'] >= min_ram]

    if 'processor' in query_params:
        processors = query_params.get('processor').lower().split(',')
        filtered_laptops = [l for l in filtered_laptops if any(p.strip() in l['processor'].lower() for p in processors)]

    return jsonify(filtered_laptops)

@app.route('/api/predict', methods=['POST'])
def predict_price():
    if not model or not preprocessor:
        return jsonify({'error': 'Model not loaded'}), 500

    features = request.get_json()
    
    # Create a feature array in the correct order for the model
    # This order must match the training data
    feature_array = np.array([[
        features.get('brand', 'Unknown'),
        features.get('processor', 'Unknown'),
        features.get('ram', 8),
        features.get('storage', 256),
        features.get('screen_size', 15.6),
        features.get('rating', 4.5)
    ]])

    # Preprocess the features
    processed_features = preprocessor.transform(feature_array)
    
    # Make prediction
    prediction = model.predict(processed_features)
    
    return jsonify({'predicted_price': round(prediction[0], 2)})

