# In a real application, these files would be generated by a separate model training script.
# For this example, we assume 'preprocessor.pkl' and 'model.pkl' exist.
try:
    with open('preprocessor.pkl', 'rb') as f:
        preprocessor = pickle.load(f)

    with open('model.pkl', 'rb') as f:
        model = pickle.load(f)
except FileNotFoundError:
    print("WARNING: 'preprocessor.pkl' or 'model.pkl' not found. The /api/predict endpoint will fail.")
